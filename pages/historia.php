<p class="txtFontC">Outro pioneiro é Antônio João, que adquiriu uma grande propriedade em 1915 pela importância de cinco contos de réis, à margem direita do Rio dos Bois. Entre os pioneiros, estão também: Antônio Martins da Rocha, José Alves de Faria, Orcino Rodrigues de Rezende, Mizael Machado, Paulo Bueno, Felipe Alves de Faria e outros. A primeira casa comercial foi de José Alves de Faria, ele chegou na região em 1913 e estabeleceu-se no ramo de comércio varejista. A emancipação política de Santo Antônio do Alegrete do município de Palmeiras de Goiás veio em 8 de outubro de 1948. A intenção dos moradores era batizar o município com o nome de Alegrete, mas como já existia uma grande cidade com este nome no Rio Grande do Sul, estes moradores entraram em um dilema. Uma das prováveis origens do nome é que alguém deveria sugerir uma idéia, e Edéia acabou como nome do município. Esta é uma das versões.
